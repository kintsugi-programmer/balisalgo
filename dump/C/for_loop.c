
#include <stdio.h>

int main()
{
   // for loop = repeats a section of code a limited amount of times
   // syntax: for (initialization; condition; final expression)
   // {        
   //    code to be executed
   // }
   

   for(int i = 1; i <= 10; i++)
   {
      printf("%d\n", i);
   }

   return 0;
}